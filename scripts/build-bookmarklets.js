const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "..");

const bookmarklets = [
    {
        key: "delete",
        source: "bookmarklets/delete.js",
        wrapper: "inline",
    },
    {
        key: "archiveDelete",
        source: "bookmarklets/archive-delete.js",
        wrapper: "inline",
    },
    {
        key: "followCleanup",
        source: "bookmarklets/follow-cleanup.js",
        wrapper: "inline",
    },
    {
        key: "likedUsers",
        source: "bookmarklets/liked-users.js",
        wrapper: "inline",
    },
    {
        key: "favoriteDelete",
        source: "bookmarklets/favorite-delete.js",
        wrapper: "plain",
    },
];

const tools = [
    {
        key: "copyTree",
        source: "bookmarklets/copy-tree.js",
    },
    {
        key: "randomReply",
        source: "bookmarklets/random-reply.js",
    },
    {
        key: "bulkDm",
        source: "bookmarklets/bulk-dm.js",
    },
    {
        key: "addBookmark",
        source: "bookmarklets/add-bookmark.js",
    },
    {
        key: "ffCleanup",
        source: "bookmarklets/ff-cleanup.js",
    },
];

function readSource(relativePath) {
    const absolutePath = path.join(root, relativePath);
    return fs.readFileSync(absolutePath, "utf8").replace(/\r\n/g, "\n").replace(/\r/g, "\n").trimEnd() + "\n";
}

function assertSyntax(label, source) {
    try {
        new Function(source);
    } catch (error) {
        error.message = `${label}: ${error.message}`;
        throw error;
    }
}

function renderGenerated() {
    const compiled = {};
    for (const bookmarklet of bookmarklets) {
        const source = readSource(bookmarklet.source);
        assertSyntax(bookmarklet.source, source);
        compiled[bookmarklet.key] = {
            wrapper: bookmarklet.wrapper,
            source,
        };
    }

    const compiledTools = {};
    for (const tool of tools) {
        const source = readSource(tool.source);
        assertSyntax(tool.source, source);
        compiledTools[tool.key] = source;
    }

    return `// Generated by scripts/build-bookmarklets.js. Edit bookmarklets/*.js instead.\n` +
        `window.bookmarkletSources = ${JSON.stringify(compiled, null, 4)};\n` +
        `window.toolBookmarkletSources = ${JSON.stringify(compiledTools, null, 4)};\n`;
}

fs.writeFileSync(path.join(root, "js/generated-bookmarklets.js"), renderGenerated());
